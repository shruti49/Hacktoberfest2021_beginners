//Template Strings uses back-ticks(``) rather than quotes to define a string
let para = `Hello World`;
console.log(para);

//with template strings, we can also use single and double quotes inside a string
let str = `India's got "talent"`;
console.log(str);

// template strings also allows multiline strings
let text = `Hello there!
How are you`;
console.log(text);



